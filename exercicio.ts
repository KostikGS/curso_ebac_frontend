// Função de multiplicação
function multiplica(num1: number, num2: number): number {
    return num1 * num2;
}

// Função de saudação
function dizOi(name: string): string {
    return `Olá ${name}`;
}

// Exemplo de uso das funções
const result = multiplica(5, 3);
console.log("Resultado da multiplicação:", result); // Saída

const saudacao = dizOi("Alice");
console.log(saudacao); // Saída